package model;

/**
 *
 * @author Admin
 */
class Parent {
    private int pid;
    private String pname;
    private String phoneNumber;

    public Parent() {
    }

    public Parent(int pid, String pname, String phoneNumber) {
        this.pid = pid;
        this.pname = pname;
        this.phoneNumber = phoneNumber;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
    
    
}
