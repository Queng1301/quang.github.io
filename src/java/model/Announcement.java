package model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Announcement {
    private int aid;
    private String title;
    private String content;
    private Date time;

    public Announcement() {
    }

    public Announcement(int aid, String title, String content, Date time) {
        this.aid = aid;
        this.title = title;
        this.content = content;
        this.time = time;
    }

    public int getAid() {
        return aid;
    }

    public void setAid(int aid) {
        this.aid = aid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }
    
}
